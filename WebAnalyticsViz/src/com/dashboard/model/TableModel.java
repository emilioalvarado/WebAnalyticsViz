package com.dashboard.model;

public class TableModel {
	String userId;
	String domainSessionId;
	String domainUserId;
	String navPath;
	String pageUrl;
	String dateStmp;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getDomainSessionId() {
		return domainSessionId;
	}
	public void setDomainSessionId(String domainSessionId) {
		this.domainSessionId = domainSessionId;
	}
	public String getDomainUserId() {
		return domainUserId;
	}
	public void setDomainUserId(String domainUserId) {
		this.domainUserId = domainUserId;
	}
	public String getNavPath() {
		return navPath;
	}
	public void setNavPath(String navPath) {
		this.navPath = navPath;
	}
	public String getPageUrl() {
		return pageUrl;
	}
	public void setPageUrl(String pageUrl) {
		this.pageUrl = pageUrl;
	}
	public String getDateStmp() {
		return dateStmp;
	}
	public void setDateStmp(String dateStmp) {
		this.dateStmp = dateStmp;
	}
	
}
